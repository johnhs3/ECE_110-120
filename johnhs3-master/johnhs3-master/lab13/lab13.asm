;Desc.:	This progam is designed to print a string of characters formed from 8*16 images
;composed of ASCII text. It will take inputs at x5000, x5001, and x5002 on until a null 
;character is detected. It will use the ASCII character placed in x5000 as the "Spaces"
;of each image, the character placed in x5001 as the "letters," and will print the null-
;terminated string starting in address x5002 to the screen using these 8*16 ascii tiles.
;
;Approach: This program uses a little bit of inspiration from Lab 12, in that the
; process for comparing the MSB of the Current Line of the Current Character is the same.
;However, it is different in that now we must iterate over multiple characters in a string
;FOR EVERY ROW, and go onto the next row when the null ASCII character is detected. To do
;this, I used 3 nested loops: 1 to iterate from rows 1-16, 1 to iterate through the 
;lines of the characters in the string, with a check to see if null has been detected,
;(if it has, prints Newline and increments row counter),
;and 1 to iterate through each of the 8 individual ASCII characters in a Char image,
;outputting a 1 or a zero by ANDing the MSB with 11111 and seeing if the result is negative.
;To get the addr. of current line to be printed, one must use the formula: 
;		A=FONT_DATA+16(ASCII CHAR)+(ROW_COUNTER)
;which I implemented, as usual, using left shift and registers. However, since row counter
;now counts up from 0 to 15, I decided to subtract this row counter value from #16 at the
;start of each outer loop to test if the program should be terminated. Below are registers:
;
;REGISTER TABLE:
;R0 | Output
;R1 | Row Counter (starts at 0, goes --> 15)
;R2 | Char address pointer (starts at x5002, gets incremented until a 0 is found)
;R3 | Column counter (starts at 8, counts down --> 0)
;R4 | ADDR. of Current Line
;R5 | DATA of Current Line
;R6 | (scratch)
;R7 | (scratch)
;-------------------------------------------------------------------

.ORIG x3000

AND R0, R0, #0 ;
AND R1, R1, #0 ;
AND R2, R2, #0 ;
AND R3, R3, #0 ;  Initialize all these registers to zero
AND R4, R4, #0 ;
AND R5, R5, #0 ;
AND R6, R6, #0 ;
AND R7, R7, #0 ;


OUTERLOOP 	ADD R6, R6, #8; Add 15 to Scratch register
		ADD R6, R6, #8;
		NOT R7, R6;		}
		ADD R7, R7, #1;		} Computes 16-(RowCounter)
		ADD R6, R1, R7;		}
		BRz FINISHED ; If R6=16-(R1) <= 0, HALT program.

		AND R7, R7, #0;
		AND R6, R6, #0; Re-Zero Scratch registers
		LD R2, IMGSTART ;Load x5002 into R2
		LDR R6, R2, #0 ;

INNERLOOP	ADD R6, R6, #0; update CC
		BRz NULLDT ; if m[x6003] (the current ASCII character) is x0000, goto Newline

		LEA R4, FONT_DATA ; Here, we are going to compute the address of the current line to print.
		LDR R6, R2, #0;	Give R6 the ASCII value stored at the address in R2
		ADD R7, R1, #0; And R7 the value of Row Counter (R1)
		ADD R6, R6, R6;
		ADD R6, R6, R6;
		ADD R6, R6, R6; Multiply R5 by #16!!!
		ADD R6, R6, R6;		
		ADD R4, R4, R6;	Add it to R4
		ADD R4, R4, R7;	Add Row Counter to R4. Now the address of the right line has been computed! 
		AND R6, R6, #0; 
		AND R7, R7, #0; Clear the Scratch regs (just to be safe)

		LDR R5, R4, #0; Use R4 as mem address to load into R5.
		AND R3, R3, #0; Clear R3
		ADD R3, R3, #8; Initialize Columm counter to 8, counting down;

INNESTLOOP	BRz ESCAPE ; if R3 = 0, we done. Goto Escape
		AND R5, R5, #-1; Check value of MSB of R4 (line)
		BRn PRINTCHAR ;	If it's a one, goto printchar
		LDI R0, SPACE ;	else load x5000 into R0 and output it
		OUT
		BRnzp SHIFTLEFT	; skip printchar
PRINTCHAR	LDI R0, CHAR ;	Load x5001 into R0 and Output it
		OUT
SHIFTLEFT	ADD R5, R5, R5; Shift Left by one bit
		ADD R3, R3, #-1; Decrement R6 innest counter
		BRnzp INNESTLOOP ; Back to Innestloop to evaluate R3


ESCAPE		ADD R2, R2, #1; Goto next Character
		LDR R6, R2, #0; Load that ASCII into R6.
		BRnzp INNERLOOP ;	Return to Inner Loop.


NULLDT		LD R0, NEWLINE ; Loads Newline Character n outputs it
		OUT ;
		ADD R1, R1, #1;
		BRnzp OUTERLOOP ;	always jump back to outerloop

FINISHED HALT ; End the program. Yay!
		

