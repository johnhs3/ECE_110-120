;----------------------------CHAR IMAGE PRINTER-------------------------------------------------------
; 		By John H. Smith, ECE 120 4-30-2020
;Description: This program takes as input the mem addresses x5000-x5002. It will, using the ASCII
;	character provided in x5002, print an 16*8 image of that single character, using x5001 as 
;	the "letter" and x5000 as the "negative space" of the image. This, effectively, will produce
;	an image of a big ASCII character made of smaller  ASCII characters.
;Approach: To implement this, one must first Obtain the inputs from x5000-x5002 and compute the address
;	of the first line of the desired image to be printed, based on the char stored in x5002.
;	(I used left shift by 4 (mult 16) and add that to FONT_DATA to do this).
;	Then, using nested loops, iterate over each of the following 16 lines, 8 characters,
;	and check if the Most significant bit of the Image line is a 0 or 1. If 1, output "CHAR."
;	If 0, output "SPACE." After every 8 characters outputted, output a new line.
;REGISTER TABLE:
;R0 | output
;R1 | line counter
;R2 | column counter
;R3 | current line ADDRESS (of image)
;R4 | current line DATA (of image)
;R5 | Scratch register
;R6 | Scratch register
;-----------------------------------------------------------------------------------------------------


.ORIG x3000

	AND R0, R0, #0	; initialize the registers to 0.
	AND R1, R1, #0	;	"
	AND R2, R2, #0	;	"
	AND R4, R4, #0	;	"
	AND R5, R5, #0	;	"
	AND R6, R6, #0	;	"
	LEA R3, FONT_DATA ; Load R3 with starting address (the Address of FONT_DATA).


	LDI R6, IMGSTART ;  Loads ascii character stored at
	ADD R6, R6, R6 ; Left shift R6 by 4 (Multiply it by 16)	
	ADD R6, R6, R6 ; 
	ADD R6, R6, R6 ;	The cheap (non-loopy) way :P
	ADD R6, R6, R6 ;	
	ADD R3, R3, R6 ; Result is added to R3, giving us the first line of the correct Char Img.

	ADD R1, R1, #8 ; Set Row Counter to 8 (going to be 16)
	ADD R1, R1, #8 ; Set Row counter (R1) to 16

	OUTERLOOP BRz FINISH
	LDR R4, R3, #0 ; Loads the data for current line address (R3) into R4
	ADD R2, R2, #8 ; Sets column counter R2 to 8

INNERLOOP BRp ESCAPE ;If the counter has not counted down to zero, DON't print newline

		LD R0, NEWLINE ; Output new line
		OUT ;	
		ADD R3, R3, #1 ; point to next Image line
		ADD R1, R1, #-1 ; decrement row counter
 		BRnzp OUTERLOOP ; end of inner loop execution

ESCAPE		ADD R4, R4, #0 ; set CC based on R4
		BRn PRINTCHAR ; if R4 is negative, then MSB is a 1, so jump to "print char" subroutine
		LDI R0, SPACE ; Loads Character (0) into R0 and prints it
		OUT

SHIFTBR 	ADD R4, R4, R4 ; shift R4 left by one bit
		ADD R2, R2, #-1 ; decrement R2
		BRnzp INNERLOOP ; jump back to INNERLOOP, begin next column

PRINTCHAR	LDI R0,CHAR ; Loads Character (1) into R0 and prints it 
		OUT
		BRnzp SHIFTBR ; Returns to the ending portion of INNERLOOP.

FINISH HALT	; Halt the program. :)

SPACE .FILL x5000 	; Label, address of Space (0) character
CHAR .FILL x5001	; Label, address of Filled (1) Character
IMGSTART .FILL x5002	; Label, address of character to be used as image template
NEWLINE .FILL x000A	; Label, ASCII character code for new line

